package entity;

public class Data {
	
	private String data;
	private String mes;
	
	public Data(String data, String mes) {
		this.data = data;
		this.mes = mes;
	}
	

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getMes() {
		return mes;
	}

	public void setMes(String mes) {
		this.mes = mes;
	}
	
	
	
	
	

}
